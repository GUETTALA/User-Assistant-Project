    public IGAInterface(List<Appariement> MatchingResult, String filePathName, List<Visualisation> DataAttributeList, List<Visualisation> VisualAttributeList, String ElemGraph, List<Normalisation> NormalisationVector) throws Exception{
        
        /* Nom de la méthode de visualisation (Element graphique de base ex. NUAGE3D_CUBE) */
        MethodeName = ElemGraph;

        /* l'adresse du fichier xml (Path of data base) */
        XMLfilepathName = filePathName;

        /* Liste des attributs de données initial avant matching */
        ListInitialDataAttribute = cloneIndividual(DataAttributeList);

        /*  Vecteur des valeurs de normalisation des attributs de données numérique
         *  Le vecteur est utilisé pour la mise à jours des noeuds  xMinVal, yMinVal, zMinVal et aussi xMaxVal, yMaxVal, zMaxVal
         *  La mise à jour se fait après chaque appariement lors de la mise à jour de la partie configuration du fichier XML
        */
        list_Numerique_DataAttribute_Normalisation = cloneNormalisationVector(NormalisationVector);

        DataAttributePopulation = new ArrayList<List<Visualisation>>();
        for(int i=0; i<PopulationSize; i++){
            DataAttributePopulation.add(ListInitialDataAttribute);
        }
        /*  Initialisation du nombre d'itération de l'algorithme génétique */
        NbreIteration = 0;
        Individual_Size = MatchingResult.size();

        initialisation();

        /* Liste des attributs visuels*/
        Initial_v_attribte = cloneIndividual(VisualAttributeList);
        

        /* Liste des attributs de données choisies lors de l'opération de mise en correspondance */
        //Initial_d_attribte = CreateDataAttributeList(MatchingResult);

        /* Créer la population initial avec comme paramétes - attributs de données et attributs visuels */
        //InitialPopulation = GenererInitialPopulation(Initial_d_attribte, Initial_v_attribte);
        InitialPopulation = GenererInitialPopulation(ListInitialDataAttribute, Initial_v_attribte);

        /* Affectation du résultat de la mise en correspondance de l'étape précédante */
        NewMatchingIndividualResult = MatchingResult;
        for(int i =0; i<8; i++){
          ReadXMLFileGA(XMLfilepathName, i);
          UpdateElement_InitialPopulation(Individual_Size, InitialPopulation, i, list_Numerique_DataAttribute_Normalisation);
          enregistreFichier(filePathName);
        }
        initComponents();
        Step5.setEnabled(false);
        Step6.setEnabled(false);
        AfficherIndividus(XMLfilepathName);

    }


