    /* Fonction qui calcule le bruit qui modifie le poids du gène à muter */
    private double Generate_NewWeidth(int oldWeidth){
        /* bruit = (r - 0.5) * 2 * alpha
         on prend omega = r - 0.5 avec r appartient à [0,1]
         on prend beta = 2 * alpha avec alpha appartient à [-10,+10]
         la fonction devient de la forme :
         * bruit = ((Math.random() - 0.5) * (2 * (Math.random() * 10)));*
         les deux variables min et max servent à corriger la valeur de l'importance obtenue
         */
        
        double newWeidth;
        double bruit = 0;
        double omega = Math.random() - 0.5; /* (r - 1) */
        double max = 100, min = 0;

        bruit = ((omega) * (2 * ALPHA));
        newWeidth = oldWeidth+bruit;
        
        if (newWeidth>100){
            newWeidth = max;
        }

        if (newWeidth<0){
            newWeidth = min;
        }
        
        return newWeidth;
    }

